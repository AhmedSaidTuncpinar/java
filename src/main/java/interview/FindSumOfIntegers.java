package interview;

public class FindSumOfIntegers {
    public static void main(String[]args){
       //find sum of the integers from 3 to 9
       int num= 0;
       for (int i=3; i<10; i++){
           num= num + i;
       }
        System.out.println(num);




       //find sum of the integers from 8 to 19
        int nm= 0;
        for (int i=8; i<20; i++){
            nm= nm + i;
        }
        System.out.println(nm);
    }

}
